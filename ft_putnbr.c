#include "libft.h"

void	ft_putnbr(int n)
{
	long d;
	char x;
	long y;

	y = (long)n;
	d = 1;
	if (n < 0)
	{
		ft_putchar('-');
		y *= -1;
	}
	if (n == 0)
		ft_putchar('0');
	while (d < y)
		d *= 10;
	d /= 10;
	while (d != 0)
	{
		x = (y / d) + 48;
		putchar(x);
		y %= d;
		d /= 10;
	}
}
