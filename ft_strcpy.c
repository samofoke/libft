#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int		x;

	x = 0;
	while (src[x] != '\0')
	{
		dst[x] = src[x];
		x++;
	}
	dst[x] = '\0';
	return (dst);
}
